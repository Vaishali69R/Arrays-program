 class Foreacharray {
    public static void main(String[] args) {
        int a[]={10,23,12,13};
        for(int i : a){
            System.out.println(i);
        }
        String ar[] = {"vish" , "maya" , "gopi"};
        for(String i : ar){
            System.out.println(i);

        }
    }
    
}
