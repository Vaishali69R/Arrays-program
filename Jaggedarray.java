 class Jaggedarray {
    public static void main(String[] args) {
        int a[][]={{1,5,7},{2,1},{5,10,11,15}};
        for(int i=0;i<a.length;i++){
            for(int j=0;j<a[i].length;j++){
                System.out.print(a[i][j]+ " ");
            }
            System.out.println();
        }
       
    }
    
}
